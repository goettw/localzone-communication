package info.localzone.communication;

public class RenderedHash {
long code;
int significantBits;
double latitudeSize;
double longitudeSize;
public long getCode() {
	return code;
}
public void setCode(long code) {
	this.code = code;
}
public int getSignificantBits() {
	return significantBits;
}
public void setSignificantBits(int significantBits) {
	this.significantBits = significantBits;
}
public double getLatitudeSize() {
	return latitudeSize;
}
public void setLatitudeSize(double latitudeSize) {
	this.latitudeSize = latitudeSize;
}
public double getLongitudeSize() {
	return longitudeSize;
}
public void setLongitudeSize(double longitudeSize) {
	this.longitudeSize = longitudeSize;
}
}
